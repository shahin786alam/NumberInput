package OopConceptPart1;

public class UnaryOperater {

	public static void main(String[] args) {
		int a=-20;
		int i;
		i=+a;
		System.out.println(i);
		i=-a;
		System.out.println(i);
		System.out.println("==================");
		int x=20;
		int y;
		
		y=x++;
		System.out.println(y);
		y=x;
		System.out.println(y);
		y=x--;
		System.out.println(y);
		y=x;
		System.out.println(y);
		y=--x;
		System.out.println(y);

	}

}
