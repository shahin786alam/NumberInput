package OopConceptPart1;

public class EscapeSaquence {

	public static void main(String[] args) {
		
		
		System.out.println("My name \nis alam");
		
		System.out.println("My name \t is alam");
		
		System.out.println("\"My name is alam\"");
		
		System.out.println("\'My name is alam\'");
		
		System.out.println("My name is alam");

	}

}
