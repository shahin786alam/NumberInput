package numberinput;

public class AssignmentOperator {

	public static void main(String[] args) {
		
		
		int x=5;
		int y=5;
		
		x+=y; //x=x+y =10
		System.out.println("x="+x);
		
		x-=y; //x=x-y  =5
		System.out.println("x="+x);
		
		x*=y; //x=x*y  =25
		System.out.println("x="+x);
		
		x/=y; //x=x/y  =5
		System.out.println("x="+x);
		
		x%=y; //x=x%y  =0
		System.out.println("x="+x);
		
	}
 }	
		
		
	
	
	
	

	
